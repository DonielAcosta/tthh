			<h1>Valores Institucionales</h1>
			<p class="texto-contenido">
			Implican la razón de ser de la Dirección, y constituyen un elemento motivador de las acciones y comportamientos de las personas, en este sentido, el personal adscrito a la Dirección Estadal del Poder Popular de Recursos Humanos posee como valores compartidos los siguientes:<br><br> 
			<ul class="texto-contenido">
				<li class="li"><b>Honestidad:</b> Implica armonizar las palabras con los hechos actuando con coherencia y apegados a los principios, con el objeto de proceder en conformidad con la misión institucional. 
				</li>
				<li class="li"><b>Colaboración:</b> Constituye el apoyo oportuno, solidario y eficaz entre quienes forman parte de la Dirección y entre éstos y las demás personas, para alcanzar así los mejores resultados. 
				</li>
				<li class="li"><b>Compromiso:</b> Se fundamenta en el convenio, obligación o palabra dada para efectuar las actividades estando conscientes de la Misión y Visión de la Dirección. </li>
				<li class="li"><b>Disciplina:</b> Conlleva el seguimiento del desarrollo de los procesos mediante las normas y estatutos establecidos por la Constitución, Leyes, Reglamentos y Decretos que rigen esta Dirección. </li>
				<li class="li"><b>Ética:</b> Ejercer la Función Pública con adhesión a rigurosos principios morales que garanticen plenamente los valores. </li>
				<li class="li"><b>Integridad:</b> Asumir con rectitud y honradez la ejecución de las actividades y procedimientos de la Dirección. </li>
				<li class="li"><b>Responsabilidad:</b> Sostener una disposición permanente de cumplir los compromisos adquiridos sin desviarse de los objetivos fundamentales.</li>
			</ul>
			</p>
