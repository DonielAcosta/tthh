			<h1>Visión</h1>
			<p class="texto-contenido">
			La Dirección Estadal del Poder Popular de Recursos Humanos tiene la visión, de hacer de este Órgano un modelo de Gerencia Institucional, brindando los mejores servicios a empleados, obreros y público en general vinculado con la Gobernación del Estado y, a la vez, contribuir con el desarrollo de los Entes Descentralizados en materia de Recursos Humanos, esto bajo un clima armónico que sirva de motivación hacia el logro de un eficiente liderazgo como institución integradora.		
			</p>
