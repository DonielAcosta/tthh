			La Dirección Estadal del Poder Popular de Recursos Humanos tiene como Misión la formulación, ejecución, control, seguimiento y evaluación, en el marco de sus competencias y la ley, de las políticas, planes, programas, proyectos y acciones en materia de personal de la Administración Pública Estadal, de conformidad con las órdenes, directrices o lineamientos recibidos por el Gobernador o Gobernadora del Estado. Así mismo dirigir o coordinar, según corresponda, la aplicación de las normas y de los procedimientos que en materia de Administración de Personal señale la ley.	
