// valida los campos
function valida_campos() 
{
  // valido que los campos tengan datos
  if (is_empty($("#correo").val())) {
      alert('Error: El campo Correo ElectrÃ³nico debe tener datos.');
      return false;
  }

  if ($("#clave").val().length < 5 || $("#clave").val().length > 10) {
      alert('Error: El campo Clave de Usuario debe tener datos.');
      return false;
  }
  return true;
}

//
$(document).ready(function () {
  // simulo un submit
  $('#login').click(function(event) {
    event.preventDefault();
    if (valida_campos()) {
       $.ajax({
         url    : '<?php echo base_url("Usuario_c/login");?>',
         data   : { 'correo' : $("#correo").val(),
                    'clave'  : $("#clave").val()
                  }, 
         type   : 'post',
         dataType : 'json',
         success  : function(resp) {
            if (resp.success) 
              $(location).attr("href","<?php echo base_url();?>");
            else
              alert(resp.mensaje);
         }
       })
    }
  });
});
</script>

